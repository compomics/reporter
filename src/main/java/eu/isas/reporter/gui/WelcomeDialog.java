    /**
     * Check for new version.
     *
     * @param searchGuiJarPath the path to the SearchGUI jar file
     * @return true if a new version is available
     */
    public boolean checkForNewSearchGUIVersion(String searchGuiJarPath) {

        try {

            File jarFile = new File(searchGuiJarPath);
            MavenJarFile oldMavenJarFile = new MavenJarFile(jarFile.toURI());

            URL jarRepository = new URL(
                    "https",
                    "genesis.ugent.be",
                    "/archiva/repository/maven2/"
            );

            return WebDAO.newVersionReleased(oldMavenJarFile, jarRepository);

        } catch (UnknownHostException ex) {

            System.out.println("Checking for new version failed. No internet connection.");
            // no internet connection
            return false;

        } catch (Exception e) {

            e.printStackTrace();
            return false;

        }
    }

    /**
     * Download SearchGUI.
     *
     * @return true if not canceled
     */
    public boolean downloadSearchGUI() {

        boolean firstTimeInstall = true;
        String installPath = null;

        UtilitiesUserParameters utilitiesUserParameters = reporterGUI.getUtilitiesUserParameters();

        if (utilitiesUserParameters.getSearchGuiPath() != null) {
            if (new File(utilitiesUserParameters.getSearchGuiPath()).getParentFile() != null
                    && new File(utilitiesUserParameters.getSearchGuiPath()).getParentFile().getParentFile() != null) {
                installPath = new File(utilitiesUserParameters.getSearchGuiPath()).getParentFile().getParent();
            }
        }

        final File downloadFolder;

        if (installPath == null) {
            installPath = "user.home";
            downloadFolder = FileChooserUtil.getUserSelectedFolder(this, "Select SearchGUI Folder", installPath, "SearchGUI Folder", "Select", false);
        } else {
            firstTimeInstall = false;
            downloadFolder = new File(installPath);
        }

        final boolean finalFirstTimeInstall = firstTimeInstall;

        if (downloadFolder != null) {

            progressDialog = new ProgressDialogX(dummyParentFrame,
                    Toolkit.getDefaultToolkit().getImage(getClass().getResource("/icons/peptide-shaker.gif")),
                    Toolkit.getDefaultToolkit().getImage(getClass().getResource("/icons/peptide-shaker-orange.gif")),
                    true);

            progressDialog.setPrimaryProgressCounterIndeterminate(true);
            progressDialog.setTitle("Downloading SearchGUI. Please Wait...");

            new Thread(new Runnable() {
                public void run() {
                    try {
                        progressDialog.setVisible(true);
                    } catch (IndexOutOfBoundsException e) {
                        // ignore
                    }
                }
            }, "ProgressDialog").start();

            Thread thread = new Thread("DownloadThread") {
                @Override
                public void run() {
                    try {
                        URL jarRepository = new URL(
                                "http", "genesis.ugent.be",
                                new StringBuilder().append("/maven2/").toString()
                        );

                        if (finalFirstTimeInstall) {

                            downloadLatestZipFromRepo(
                                    downloadFolder,
                                    "SearchGUI",
                                    "eu.isas.searchgui",
                                    "SearchGUI",
                                    "searchgui.ico",
                                    null,
                                    jarRepository,
                                    false,
                                    true,
                                    new GUIFileDAO(),
                                    progressDialog
                            );

                        } else {

                            downloadLatestZipFromRepo(
                                    new File(reporterGUI.getUtilitiesUserParameters().getSearchGuiPath()).toURI().toURL(),
                                    "SearchGUI",
                                    false,
                                    "searchgui.ico",
                                    null, jarRepository,
                                    false,
                                    true,
                                    new GUIFileDAO(),
                                    progressDialog
                            );

                        }

                    } catch (IOException e) {
                        e.printStackTrace();
                    } catch (URISyntaxException e) {
                        e.printStackTrace();
                    } catch (XMLStreamException e) {
                        e.printStackTrace();
                    }
                }
            };
            thread.start();

            try {
                thread.join();
            } catch (InterruptedException e) {
                e.printStackTrace();
            }

            if (progressDialog.isRunCanceled()) {

                progressDialog.setRunFinished();
                return false;

            } else {

                if (!progressDialog.isRunFinished()) {
                    progressDialog.setRunFinished();
                }

            }

            return true;
        }

        return false;
    }
